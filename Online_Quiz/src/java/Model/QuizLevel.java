package Model;

/**
 *
 * @author hongd
 */
public class QuizLevel {
    private int id;
    private String level;

    public QuizLevel() {
    }

    public QuizLevel(int id, String level) {
        this.id = id;
        this.level = level;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }
    
    
}
